from agent.executor import run_agent

CHAT_ID = 1032083811
PASS = "✅"
FAIL = "❌"
results = []


async def ask(label: str, msg: str, expect: list[str] = None, not_expect: list[str] = None) -> str:
    print(f"\n{'─'*60}")
    print(f"[TEST] {label}")
    print(f"[IN]   {msg}")
    try:
        resp = await run_agent(CHAT_ID, msg)
        print(f"[OUT]  {resp[:300]}")

        ok = True
        if expect:
            for kw in expect:
                if kw.lower() not in resp.lower():
                    print(f"  {FAIL} Ожидалось: {kw!r}")
                    ok = False
        if not_expect:
            for kw in not_expect:
                if kw.lower() in resp.lower():
                    print(f"  {FAIL} НЕ должно быть: {kw!r}")
                    ok = False

        status = PASS if ok else FAIL
        print(f"  {status} {label}")
        results.append((status, label))
        return resp
    except Exception as e:
        print(f"  {FAIL} EXCEPTION: {e}")
        results.append((FAIL, f"{label} — {e}"))
        return ""


